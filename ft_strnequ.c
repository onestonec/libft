#include "libft.h"

int     ft_strnequ(char const *s1, char const *s2, size_t n){
        
	if(!s1 || !s2){
		return 0;
	}

        for(size_t i = 0; (i < n) && (s1[i]) && (s2[i]); i++){
                if(s1[i] != s2[i]){
                        return 0;
                }
        }
        return 1;
}
